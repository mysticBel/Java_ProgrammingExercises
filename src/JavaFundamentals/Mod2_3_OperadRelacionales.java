package JavaFundamentals;

public class Mod2_3_OperadRelacionales {
    public static void main(String[] args) {
        // Operadores Relacionales

        int var1 = 300, var2 = 200;
        boolean resultado;

        // O.R : <, >, <=, >= , ==, !=
        resultado = var1 > var2;
        System.out.println(resultado);

        resultado = var1 == var2;
        System.out.println(resultado);
    }

}
