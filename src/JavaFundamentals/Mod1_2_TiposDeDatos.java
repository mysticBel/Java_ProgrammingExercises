package JavaFundamentals;

public class Mod1_2_TiposDeDatos {
    public static void main(String[] args) {

        //Correcto
        float edad = 18f;
        //Incorrecto
        //int edad = 18.5;

        /*
         * Tipos Primitivos
         * Byte, Short, int, lomg
         * char, float, double, boolean
         * */

        int versionInt = 9;
        float versionEspecifica = 9.8f;
        System.out.println(versionInt);
        System.out.println(versionEspecifica);

        char letra = 'a';
        char numeroChar = 97;
        System.out.println(letra);
        System.out.println(numeroChar);

        boolean valorVerd = true;
        boolean valorFalso = false;
        System.out.println(valorVerd);
        System.out.println(valorFalso);
    }
}
